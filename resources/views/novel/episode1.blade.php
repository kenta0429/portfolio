<div id="novel_honbun" class="novel_view">
<p id="L1">　小さなときから、とにかく魔法使いになりたかった。</p>
<p id="L2"><br /></p>
<p id="L3">　王子様と結婚して幸せになるお姫様よりも</p>
<p id="L4">　お姫様にドレスを与える魔法使いに。</p>
<p id="L5">　いたいけな子供を食べようとする魔女だって構わない。</p>
<p id="L6">　魔法を使って敵を倒しまくる魔法少女も楽しそう。</p>
<p id="L7"><br /></p>
<p id="L8">　小さなときから</p>
<p id="L9">　ずっと、ずっと、ずぅーっと、魔法使いになりたかった。</p>
<p id="L10"><br /></p>
<p id="L11"><br /></p>
<p id="L12"><br /></p>
<p id="L13"><br /></p>
<p id="L14"><br /></p>
<p id="L15"><br /></p>
<p id="L16"><br /></p>
<p id="L17"><br /></p>
<p id="L18">　誰かの強い想いが急激に流れ込んできて、わたしは思わず頭を抱える。</p>
<p id="L19"><br /></p>
<p id="L20">　な、に……これ……っ！</p>
<p id="L21"><br /></p>
<p id="L22">　大量の映像で頭の中が埋め尽くされる。</p>
<p id="L23">　視界が歪んで立っていられない！</p>
<p id="L24">　どうしようと焦る気持ちさえ、追いつかない思考に飲み込まれていく。</p>
<p id="L25">　耳元でキーンと高い音が響き、意識が遠のく。</p>
<p id="L26"><br /></p>
<p id="L27">　誰か……たすけ、て……</p>
<p id="L28"><br /></p>
<p id="L29">　ぐらりと体が傾いたのだけは、わかった。</p>
<p id="L30"><br /></p>
<p id="L31"><br /></p>
<p id="L32"><br /></p>
<p id="L33"><br /></p>
<p id="L34"><br /></p>
<p id="L35"><br /></p>
<p id="L36"><br /></p>
<p id="L37"><br /></p>
<p id="L38"><br /></p>
<p id="L39"><br /></p>
<p id="L40"><br /></p>
<p id="L41"><br /></p>
<p id="L42">「目は覚めたかしら？」</p>
<p id="L43"><br /></p>
<p id="L44">　気が付いたら、見慣れた天井と、少し困ったように微笑むエレノグレース様が目に入った。</p>
<p id="L45"><br /></p>
<p id="L46">「わたし……」</p>
<p id="L47"><br /></p>
<p id="L48">「遊んでいたら、倒れたそうよ。小鳥たちが知らせてくれたわ」</p>
<p id="L49"><br /></p>
<p id="L50">　そうだ……大量の映像が流れてきて……。</p>
<p id="L51">　思い出そうとすると、痛くなるような気がして頭を抑える。</p>
<p id="L52"><br /></p>
<p id="L53">「リリマリア、少し困ったことになったわ。顔色も良くないのに、かわいそうだけれど話を聞いてくれるかしら？」</p>
<p id="L54"><br /></p>
<p id="L55">　エレノグレース様は、眉尻を下げてさらに困ったような笑顔になる。</p>
<p id="L56">　困ったことですか？</p>
<p id="L57">　ベッドから起き上がり「はい」と告げると、ゆっくりと話し始めるエレノグレース様に向き直った。</p>
<p id="L58"><br /></p>
<p id="L59"><br /></p>
<p id="L60"><br /></p>
<p id="L61">　エレノグレース様の話は、わたしの理解を超えていて衝撃に固まるしかなかった。</p>
<p id="L62">　まず、わたしが前世の記憶を取り戻した転生者であると説明された。流れ込んだきた映像が、前世の記憶らしい。</p>
<p id="L63">　そして、倒れていたわたしは白い光に包まれていて、魔力を授けられたようだと言われた。それも将来が楽しみなくらいの魔力量だそうだ。</p>
<p id="L64">　さらに、魔力を持っている子供は７歳になると魔法学院に入学しなければならないと決まりがあり、６歳のわたしは、早急に準備を整えなければならないことを教えられた。</p>
<p id="L65"><br /></p>
<p id="L66">　前世の記憶に転生者、魔力に魔法学院？</p>
<p id="L67">　情報が多すぎて処理できない！</p>
<p id="L68"><br /></p>
<p id="L69">　きわめつけに、エレノグレース様も転生者だと告白された。</p>
<p id="L70">　魔法学院に行くまでの間、準備だけでなく先輩として「いろんな話をしましょう」とエレノグレース様は目を輝かせて微笑んだ。その笑顔はとても可愛らしくて、わたしは思わず声をあげて笑ってしまった。</p>
<p id="L71"><br /></p>
<p id="L72">「……急にたくさんのことを話してしまってごめんなさいね。まだ、前世の記憶が自分の記憶であることの自覚がないようだし、今日はこのまま休みなさい。ゆっくりと記憶と向き合ってみるといいと思うわ。そうね、三日後！三日後には魔法学院に入学するための準備をはじめましょう！」</p>
<p id="L73"><br /></p>
<p id="L74"><br /></p>
<p id="L75"><br /></p>
<p id="L76">　部屋から送り出されたわたしは、自室に戻ってベッドに腰かけた。倒れこむと、エレノグレース様が好きな花の香りがする。シーツに花の香りが付けられているのだ。</p>
<p id="L77">　不思議なことをたくさん知っている方だと思っていたけど、転生者だからだったのか。</p>
<p id="L78">　……自分も同じなんて信じられないけれど。</p>
<p id="L79">　ゆっくり息を吸い込んで、目を閉じる。</p>
<p id="L80">　教えてもらったとおり、ゆっくり深呼吸を繰り返しながら、前世の記憶に意識を集中させていった。</p>
<p id="L81"><br /></p>
<p id="L82"><br /></p>
<p id="L83"><br /></p>
<p id="L84">「こんにちは。新しいわたしは、リリマリアって名前なんだねっ！」</p>
<p id="L85"><br /></p>
<p id="L86">　そこには、髪を短く整えたさっぱりと笑う女性がいた。</p>
<p id="L87">　この人が前世のわたし……？</p>
<p id="L88"><br /></p>
<p id="L89">「わたしは、朝田　都子、２２歳。日本で、大学生をしていたよ。うーん。いろいろ話してもいいんだけど、どうせ同化するんだし、その方が早いよね？どう思う？」</p>
<p id="L90"><br /></p>
<p id="L91">「はあ……」</p>
<p id="L92"><br /></p>
<p id="L93">　コロコロと表情を変えながらたたみ掛けるように話す前世のわたし。目線を合わせるようにしゃがみ込むと、今のわたしの手を取った。</p>
<p id="L94"><br /></p>
<p id="L95">「リリマリアちゃん。わたしはすごく嬉しい。わたしの夢は魔法使いになることだったの。転生するときに、魔力を授けてもらって魔法使いを目指すことができるようになった。あなたにとって新しい力だけど、役に立つはず。これから同化するけれど、わたしの記憶を持ったリリマリアとして生きて！魔法使いになるという夢だけは叶えてくれたら嬉しい」</p>
<p id="L96"><br /></p>
<p id="L97">　まっすぐ向けられた目と真剣な声。まるで消えてしまうようで、なんだか少し寂しくなる。</p>
<p id="L98"><br /></p>
<p id="L99">「わたしたちは、同じなのですよね？魔法使いになる夢を一緒に叶えましょう！」</p>
<p id="L100"><br /></p>
<p id="L101">　わたしは、ぐっと声に力を込めて、強く言い切った。</p>
<p id="L102">　目を見開いた前世のわたしは、嬉しそうににかっと笑う。</p>
<p id="L103"><br /></p>
<p id="L104">「リリマリアちゃん、ありがとう。わたしはあなたの中で全部見ていたけれど、わたしの記憶はきっと不思議なことがいっぱいのはずよ。転生者の先輩、エレノグレース様とたくさん話をしましょうね」</p>
<p id="L105"><br /></p>
<p id="L106">「はい！」</p>
<p id="L107"><br /></p>
<p id="L108"><br /></p>
<p id="L109"><br /></p>
<p id="L110"><br /></p>
<p id="L111"><br /></p>
<p id="L112">　こうして、わたしは前世の記憶を取り戻し</p>
<p id="L113">　魔法使いを目指すことを決めた。</p>
